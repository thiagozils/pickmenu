package br.org.catolicasc.modelaux;

public class CardapioAux {
	

	private Long id;
	private String pratos;
	private Long restaurante;
	private String data;
	private double preco;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getPratos() {
		return pratos;
	}
	public void setPratos(String pratos) {
		this.pratos = pratos;
	}
	public Long getRestaurante() {
		return restaurante;
	}
	public void setRestaurante(Long restaurante) {
		this.restaurante = restaurante;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	
	
	
	
}